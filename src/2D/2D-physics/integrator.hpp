template<typename State, typename DerivFunc>
State rk4_step(const State& state, double h, DerivFunc f) {
    auto k1 = f(state);
    auto k2 = f(state + k1 * (h * 0.5));
    auto k3 = f(state + k2 * (h * 0.5));
    auto k4 = f(state + k3 * h);
    return state + (k1 + k2 * 2.0 + k3 * 2.0 + k4) * (h / 6.0);
}

// Runge-Kutta-Fehlberg 4(5) adaptive integrator.
// Returns the 4th-order result and writes the local truncation error
// estimate into *errOut (max absolute component-wise error).
// The caller decides whether to accept the step or shrink h.
// Butcher tableau coefficients are the classic Cash-Karp variant.
template<typename State, typename DerivFunc>
State rkf45_step(const State& state, double h, DerivFunc f, double* errOut) {
    assert(errOut && "rkf45_step requires a non-null errOut"); // null check for errOut pointer, meant to prevent silent bugs where 
    auto k1 = f(state);                                        // caller forgets to provide an error output variable and we end up writing to a random memory location. If this assert fails, check the call site of rkf45_step and make sure you're passing a valid pointer for errOut.
    auto k2 = f(state + k1 * (h * (1.0/5.0)));
    auto k3 = f(state + k1 * (h * (3.0/40.0))   + k2 * (h * (9.0/40.0)));
    auto k4 = f(state + k1 * (h * (3.0/10.0))    + k2 * (h * (-9.0/10.0))   + k3 * (h * (6.0/5.0)));
    auto k5 = f(state + k1 * (h * (-11.0/54.0))  + k2 * (h * (5.0/2.0))     + k3 * (h * (-70.0/27.0))  + k4 * (h * (35.0/27.0)));
    auto k6 = f(state + k1 * (h * (1631.0/55296.0)) + k2 * (h * (175.0/512.0)) + k3 * (h * (575.0/13824.0)) + k4 * (h * (44275.0/110592.0)) + k5 * (h * (253.0/4096.0)));

    // 4th-order solution. Canonical Cash-Karp b* row:
    //   2825/27648, 0, 18575/48384, 13525/55296, +277/14336, 1/4
    // The k5 weight is POSITIVE. The row sums to 1 (order-1 consistency); any
    // other sign breaks consistency and makes y4 a different method whose
    // y5 - y4 difference no longer estimates the embedded O(h^5) truncation
    // error — it carries a spurious O(h) floor instead.
    auto y4 = state + (k1 * (2825.0/27648.0) + k3 * (18575.0/48384.0) + k4 * (13525.0/55296.0) + k5 * (277.0/14336.0) + k6 * (0.25)) * h;
    // 5th-order solution
    auto y5 = state + (k1 * (37.0/378.0) + k3 * (250.0/621.0) + k4 * (125.0/594.0) + k6 * (512.0/1771.0)) * h;

    // Error estimate: difference between 5th and 4th order
    auto diff = y5 + y4 * (-1.0);
    *errOut = std::max({std::abs(diff.r), std::abs(diff.phi), std::abs(diff.vr)});

    return y5;  // return the higher-order result
}
